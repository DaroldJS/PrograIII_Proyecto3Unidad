    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        int cantidadc = (int) (SpnCantidadComprar.getValue());
        int stock = Integer.parseInt(txtStock.getText());
        if (stock >= cantidadc) {
            boolean productoentabla = false;
            int nrofila = 5;
            for (int i = 0; i < tbCompra.getRowCount(); i++) {
                String codigo = tbCompra.getValueAt(i, 0).toString();
                if (codigo.equals(txtCodigoProducto.getText())) {
                    productoentabla = true;
                    nrofila = i;
                    break;
                }
            }

            double preciouni = Double.parseDouble(txtPrecioUnitario.getText());
            double total = Double.parseDouble(txtTotalPagar.getText());
            double subtotal = cantidadc * preciouni;
            if (productoentabla) {
                int tcantidad = Integer.parseInt(tbCompra.getValueAt(nrofila, 2).toString());
                double tsubtotal = Double.parseDouble(tbCompra.getValueAt(nrofila, 4).toString());

                int nuevacant = cantidadc + tcantidad;
                double nuevosub = tsubtotal + subtotal;

                tbCompra.setValueAt(nuevacant, nrofila, 2);
                tbCompra.setValueAt(nuevosub, nrofila, 4);

            } else {
                prueba[0] = txtCodigoProducto.getText();
                prueba[1] = txtNombreProducto.getText();
                prueba[2] = String.valueOf(cantidadc);
                prueba[3] = String.valueOf(preciouni);
                prueba[4] = String.valueOf(subtotal);
                modelo.addRow(prueba);
            }

            // Actualizar total a pagar
            double nuevototal = total + subtotal;
            txtTotalPagar.setText(String.valueOf(nuevototal));

            // Actualizar stock
            int nuevostock = stock - cantidadc;
            txtStock.setText(String.valueOf(nuevostock));
        } else {
            JOptionPane.showMessageDialog(null, "La Cantidad a comprar no debe ser mayor al stock");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarActionPerformed
        int filam;
        filam = tbCompra.getSelectedRow();
        if (filam != -1) {
            String codigo = tbCompra.getValueAt(filam, 0).toString();
            if (codigo.equals(prueba[0])) {
                int stock = Integer.parseInt(txtStock.getText());
                int cantidad = Integer.parseInt(tbCompra.getValueAt(filam, 2).toString());
                double subtotal = Double.parseDouble(tbCompra.getValueAt(filam, 4).toString());
                double total = Double.parseDouble(txtTotalPagar.getText());

                int nstock = stock + cantidad;
                double ntotal = total - subtotal;
                txtStock.setText(String.valueOf(nstock));
                txtTotalPagar.setText(String.valueOf(ntotal));
                modelo.removeRow(filam);
            }
        } else {
            JOptionPane.showMessageDialog(null, "SELECCIONA UNA FILA");
        }
    }//GEN-LAST:event_btnQuitarActionPerformed

