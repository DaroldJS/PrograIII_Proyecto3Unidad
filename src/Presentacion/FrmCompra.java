    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        // COMPRA
        ClsECompra objEC = new ClsECompra();
        ClsNCompra objNC = new ClsNCompra();
        objEC.setTotal(Double.parseDouble(txtTotalPagar.getText()));
        objEC.setId_empleado(Principal.IdEmpleado2);
        objEC.setId_proveedor(Principal.IdProveedor2);

        if (objNC.MtdResgistrarCompra(objEC)) {
            JOptionPane.showMessageDialog(null, "VENTA AGREGADA");
            btnAgregar.setEnabled(false);
            btnQuitar.setEnabled(false);
            //            Principal.montocaja = Principal.montocaja + objEE.getTotalPagar();
            //            FrmCaja.txtMonto.setText(String.valueOf(Principal.montocaja));
        } else {
            JOptionPane.showMessageDialog(null, "NO SE PUDO GUARDAR DATOS");
            return;
        }

        // DETALLE DE COMPRA
        ClsEDetalleCompra objED = new ClsEDetalleCompra();
        ClsNDetalleCompra objND = new ClsNDetalleCompra();
        ClsNProducto objNP = new ClsNProducto();
        int nfilas = tbCompra.getRowCount();
        for (int i = 0; i < nfilas; i++) {
            String codigo = tbCompra.getValueAt(i, 0).toString();
            objED.setCantidad(Integer.parseInt(tbCompra.getValueAt(i, 2).toString()));
            objED.setProducto(objNP.BuscarProductoPorCodigo(codigo));
            objED.setSubtotal(Double.parseDouble(tbCompra.getValueAt(i, 4).toString()));
            objED.setCompra(new ClsECompra());
            objED.getCompra().setId_compra(objNC.MtdBuscarUltimaCompra());
            if (!objND.MtdResgistrarDetalle(objED)) {
                JOptionPane.showMessageDialog(null, "NO SE PUDO GUARDAR DATOS");
                return;
            }
        }
        MtdLimpiar();
    }//GEN-LAST:event_btnGenerarActionPerformed


