    private void MtdListar() {
        ClsNProducto objNE = new ClsNProducto();
        String[] datos = new String[7];
        LimpiarTabla();
        for (ClsEProducto objE : objNE.MtdListarProducto()) {
            if (objE.isEstado()) {
                datos[0] = String.valueOf(objE.getId_producto());
                datos[1] = objE.getCodigo();
                datos[2] = objE.getNombre();
                datos[3] = String.valueOf(objE.getCantidad());
                datos[4] = String.valueOf(objE.getPrecio_unitario());
                modelo.addRow(datos);
            }

        }
        this.tbConsultarProducto.setModel(modelo);
    }

    private void LimpiarTabla() {
        modelo = (DefaultTableModel) tbConsultarProducto.getModel();
        modelo.setRowCount(0);
    }
